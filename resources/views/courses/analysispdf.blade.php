                <h3><b>TThe “Problem” with Data Analysis</b></h3>
                <p>
            What does ‘data analysis’ mean? Does it refer to one method or many? A collection of
            different procedures? Is it a process? If so, what does that mean? More important, can
            MSHS program staff – without a background in math or statistics – learn to identify and
            use data analysis in their work? (P.S. - the answer to the last question is Yes! – assuming a
            minimum investment of time, effort, and practice).<br>
            Data analysis can refer to a variety of specific procedures and methods. However, before
            programs can effectively use these procedures and methods, we believe it is important to
            see data analysis as part of a process. By this, we mean that data analysis involves goals;
            relationships; decision making; and ideas, in addition to working with the actual data
            itself. Simply put, data analysis includes ways of working with information (data) to
            support the work, goals and plans of your program or agency..
                </p>
                <h3><b>Data Analysis as a Linear Process</b></h3>
                <p>
                A strictly linear approach to data analysis is to work through the components in order,
                from beginning to end. A possible advantage of this approach is that it is structured and
                organized, as the steps of the process are arranged in a fixed order. In addition, this linear
                conceptualization of the process may make it easier to learn. A possible disadvantage is
                that the step-by-step nature of the decision making may obscure or limit the power of the
                analyses – in other words, the structured nature of the process limits its effectiveness.
                </p>

                <h3><b>Data Analysis as a Cycle</b></h3>
                <p>
                    A cyclical approach to data analysis provides much more flexibility to the nature of
                    the decision making and also includes more and different kinds of decisions to be made.
                    In this approach, different components of the process can be worked on at different
                    times and in different sequences – as long as everything comes “together” at the end. A
                    possible advantage of this approach is that program staff are not “bound” to work on
                    each step in order. The potential exists for program staff to “learn by doing” and to make
                    improvements to the process before it is completed.
                </p>
