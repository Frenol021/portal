                <h3><b>Developing a Team</b></h3>
                <p>
                    Similar to the Community Assessment, in which the Migrant and Seasonal Head Start
                    Director has the responsibility of ensuring that the data analysis is fully completed and
                    implemented, we would suggest the same for the data analysis. Ultimately the Migrant
                    and Seasonal Head Start Director is responsible for assuring that the data be gathered
                    and analyzed, is ready to present to appropriate stakeholders, and ready to be used for
                    program decision making and enhancement.<br>
                    Although the director holds the final responsibility for the data analysis process and
                    products, other staff are active participants in the collection and analysis of data.
                    Programs may choose to use existing policy groups to implement the data analysis
                    process, or establish a separate data analysis team. The Policy Council and Board are
                    consulted regarding the plan for implementing the decisions ascertained from the data
                    analysis, and ultimately both groups review and approve all decisions that are made.
                    The data analysis process must be designed to include a variety of stakeholders.<br>
                    Stakeholders are individuals that have a vested interest in the Migrant and Seasonal
                    Head Start Program – they will benefit from the program in some way. Migrant and
                    Seasonal Head Start programs generally operate using a systems approach, which means
                    that there already exist several teams and committees made up of various levels of the
                    organization including staff, parents, Board and Policy Council and representatives from
                    the community. Various individuals can be identified to form a data analysis team and
                    select a team leader.
                </p>
                <h3><b>Planning</b></h3>
                <p>
                    Planning is a very integral part of the data analysis process. It is imperative that you
                    develop a plan to engage in data analysis. The plan should outline your data analysis
                    process and include the purpose, questions, data collection methods, needed resources,
                    identified lead person, as well as a timetable for completing all of the necessary tasks. The
                    reality within most programs is that individuals are always managing a variety of different
                    tasks at any given time. The actual process of data analysis should be given priority.
                    Allow time for the identified person to have time or the person’s supervisor can reassign
                    certain tasks so that they can devote the appropriate amount of time to the project.
                </p>
                <h3><b>Data Management</b></h3>
                <p>
                    Data comes from many places and a variety of formats. It is important to take a
                    systematic approach and think about the following questions:
                    •How will you keep track of all the data?<br>
                    •How will you make the data available to several members of your team?<br>
                    •How will you collect and integrate data over a period of time?<br>
                    •How can you set up the system so that others can step in if your job responsibilities change?
                </p>
                 <h3><b>Writing</b></h3>
                 <p>
                    Write your key findings and interpretations as related to your purpose. A well written
                    report ought to proceed from your findings and interpretations to recommendations
                    that are clear and concise. The report should include a brief narrative consisting
                    of a restatement of your purpose(s), question(s), finding(s), interpretation(s) and recommendation(s).
                 </p>
                   <h3><b>Evaluation</b></h3>
                   <p>
                    As identified in Section 3 Conceptualizing Data Analysis as a Process, the final step of
                    the Managing the Data Analysis Process is evaluation. Here, the data analysis team can
                    review and reflect upon:<br>
                    •Development of a team: was team structure and team make-up appropriate?<br>
                    •Planning: was the plan followed, timelines met?<br>
                    •Data Management: How well was the data stored? Did the appropriate people have access to data when needed? Was the appropriate computer software used, was it user
                    friendly and were you able to go about finding needed information?<br>
                    •Writing, Reporting: How well did the writing tell the story of the data? Did the
                    intended audience find the presentation of information effective? Was the report well written?
                   </p>
